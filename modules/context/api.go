package context

import (
	"gopkg.in/macaron.v1"
)

type APIContext struct {
	*Context
}

func APIContexter() macaron.Handler {
	return func(c *Context) {
		ctx := &APIContext{
			Context: c,
		}
		c.Map(ctx)
	}
}
